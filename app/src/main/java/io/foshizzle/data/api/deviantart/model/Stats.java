/**
 * Models links to the various quality of images of a Stats.
 */
public class Stats implements Parcelable {


    private final int comments;
    private final int favourites;

    public Stats(int comments, int favourites) {
        this.comments = comments;
        this.favourites = favourites;
    }
    protected Stats(Parcel in) {
        comments = in.readInt();
        favourites = in.readInt();
    }


    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel dest, int flags) {
        dest.writeInt(comments);
        dest.writeInt(favourites);
    }

    @SuppressWarnings("unused")
    public static final Creator<Stats> CREATOR = new Creator<Stats>() {
        @Override
        public Stats createFromParcel(Parcel in) {
            return new Stats(in);
        }

        @Override
        public Stats[] newArray(int size) {
            return new Stats[size];
        }
    };

    public int getLikes() {
        return favourites;
    }

    public int getComments() {
        return comments;
    }
}
